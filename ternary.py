# ternary operator singkatnya merupakan bentuk singkat dari if else jika kondisinya hanya memiliki 1 kondisi jika ga bener berarti masuk ke else

# program bilangan genap dan ganjil
inputan = int(input("Masukan angka : "))

hasil = "Bilangan genap" if inputan % 2 == 0 else "Bilangan ganjil"

print(hasil) 