# aritmatika merupakan operator yang bertugas mengoperasikan oeprasi matematika

# tambah
a = 5
b = 4
hasil = a+b
print(hasil)

# kurang
a = 10
b = 2
hasil = a-b
print(hasil)

# kali
a = 10000
b = 2
hasil = a*b
print(f"{hasil:,}")

# bagi
a = 10000
b = 2
hasil = a/b
print(f"{hasil:,}")

# pembagian bulat (floor division)
a = 10000
b = 2
hasil = a//b
print(f"{hasil:,}")

# sisa bagi(modulus)
a = 10
b = 3
hasil = a%b
print(hasil)

# pangkat (exponen)
a = 2
b = 4
hasil = a**b
print(hasil)


